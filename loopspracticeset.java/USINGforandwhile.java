public class USINGforandwhile {
    public static void main(String[] args) {
        System.out.println("Hello, Java Oracle!");
        
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("give me a breaks girl, here we go");
        int a=0;
        while(a<=11)
        {
            System.out.println(a);
            a++;
        }
    }
    
}
